  dificuldade: 'facil',
  tipo_refeicao: 'snacks', // Sobremesa fit
  tags: ['Sobremesa fit', 'Sem açúcar', 'Proteica', 'Sem cocção'],
  is_premium: true, // Receita PREMIUM por padrão
  is_free: false,
  // Informações nutricionais (por unidade - 1 brigadeiro)
  calorias: 95, // Média entre 90-100 kcal
  carboidratos: 5, // Média entre 4-6 g
